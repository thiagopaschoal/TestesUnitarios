package br.ce.wcaquino.utils;

public class NumberUtils {

    private NumberUtils() {
    }

    public static String getUnitInRomanNumeral(int number) {
        switch(number) {
            case 1:
                return "I";
            case 2:
                return "II";
            case 3:
                return "III";
            case 4:
                return "IV";
            case 5:
                return "V";
            case 6:
                return "VI";
            case 7:
                return "VII";
            case 8:
                return "VIII";
            case 9:
                return "IX";
            default:
                throw new IllegalArgumentException("invalid number");
        }
    }

    public static String getTenInRomanNumeral(int number) {
        switch(number) {
            case 1:
                return "X";
            case 2:
                return "XX";
            case 3:
                return "XXX";
            case 4:
                return "XL";
            case 5:
                return "L";
            case 6:
                return "LX";
            case 7:
                return "LXX";
            case 8:
                return "LXXX";
            case 9:
                return "XC";
            default:
                throw new IllegalArgumentException("invalid number");
        }
    }

    public static String getCentInRomanNumeral(int number) {
        switch(number) {
            case 1:
                return "C";
            case 2:
                return "CC";
            case 3:
                return "CCC";
            case 4:
                return "CD";
            case 5:
                return "D";
            case 6:
                return "DC";
            case 7:
                return "DCC";
            case 8:
                return "DCCC";
            case 9:
                return "CM";
            default:
                throw new IllegalArgumentException("invalid number");
        }
    }
}
